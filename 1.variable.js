// ? variable naming convention
// 1. variable name must not be reserved keyword
// e.g let,const,for, if, else, switch ...

// 2. variable name must not start with number
// e.g 1name, 100student
// valid variable name student1, laptop101

// 3. variable name must not include special characters like @,!, %, &, #
// except $ and _

// let first_name$ = 'Rohan';
// let $ = 5;
// let _ = 9;

// let _ = 'Rohan';
// console.log(_);

// 4. variable name should be camelCase
// ? upperCase => ISUSER
// ? lowerCase => isuser
// ? camelCase => isUser, firstUser, isGraduated, laptopDetails

// hasSubscription
